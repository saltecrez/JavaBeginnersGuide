package chapter2;

class CharArithDemo {

    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("ch contains " + ch);
        ch++;
        System.out.println("ch is now " + ch);
        ch = 90; //ASCII
        System.out.println("ch is now " + ch);
    }
}
